package vo;

public class LineVO {
	private int no_p;
	private int buyers;
	private String buyers_nickname;
	private String buyers_phone;
	private int line_number;
	
	public int getNo_p() {
		return no_p;
	}
	public void setNo_p(int no_p) {
		this.no_p = no_p;
	}
	public int getBuyers() {
		return buyers;
	}
	public void setBuyers(int buyers) {
		this.buyers = buyers;
	}
	public String getBuyers_nickname() {
		return buyers_nickname;
	}
	public void setBuyers_nickname(String buyers_nickname) {
		this.buyers_nickname = buyers_nickname;
	}
	public String getBuyers_phone() {
		return buyers_phone;
	}
	public void setBuyers_phone(String buyers_phone) {
		this.buyers_phone = buyers_phone;
	}
	public int getLine_number() {
		return line_number;
	}
	public void setLine_number(int line_number) {
		this.line_number = line_number;
	}
}
